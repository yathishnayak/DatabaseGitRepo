/** 
Declare 
	@UserKey		INT = 1144,
	@Status			BIT	= 0,
	@Reason			VARCHAR(1000) = '',
	@IsDebug		BIT = 0,
	@JSONSTRING		NVARCHAR(Max) = ''
	EXEC [CarrierPayType_list_V2] @Userkey, @JSONSTRING, @Status OUTPUT, @Reason Output, @IsDebug
	SELECT @Status AS Status, @Reason AS Reason
**/
CREATE PROCEDURE [dbo].[CarrierPayType_list_V2]
(
	@UserKey		INT = 0,
	@JSONString		NVARCHAR(MAX) = '',
	@Status			BIT	= 0 OUTPUT,
	@Reason			VARCHAR(1000) = '' OUTPUT,
	@IsDebug		BIT = 0 
)
AS 
BEGIN
	SET NOCOUNT ON
	SET FMTONLY OFF

	SELECT PayTypeKey, PayTypeName FROM Carrier_PayTypes WITH (NOLOCK)
	WHERE isActive = 1
	FOR JSON PATH

	SET @Status = 1
	SET @Reason = 'Success'
END